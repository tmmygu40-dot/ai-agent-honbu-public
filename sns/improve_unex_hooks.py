def main():
    with open(UNEX, encoding='utf-8') as f:
        u = json.load(f)
    candidates = u['candidates']

    before_after = []
    updated = 0

    for c in candidates:
        rank = c.get('rank')
        if rank not in UPDATES:
            continue
        new_hook, new_desc = UPDATES[rank]
        before_after.append({
            'rank': rank,
            'app_name': c['app_name'],
            'before_hook': c.get('hook', ''),
            'after_hook': new_hook,
            'before_desc': c.get('description', ''),
            'after_desc': new_desc,
        })
        c['hook'] = new_hook
        c['description'] = new_desc
        updated += 1

    # meta 更新（version 引き上げ・url_verified は保持）
    u['meta']['version'] = 5
    u['meta']['hooks_revised_at'] = '2026-06-12'
    u['meta']['hooks_revised_count'] = updated
    u['meta']['hooks_revision_style'] = 'no-cat / strong-aruaru / weak-description'

    with open(UNEX, 'w', encoding='utf-8') as f:
        json.dump(u, f, ensure_ascii=False, indent=2)

    # url_verified の保持確認
    ok = sum(1 for c in candidates if c.get('url_verified') is True)
    print(f'=== K-161 hook 改善 結果 ===')
    print(f'  更新件数: {updated}')
    print(f'  url_verified=true 保持: {ok}/{len(candidates)}')
    print(f'  meta.version: {u["meta"]["version"]}')
    print()

    # 代表10件 before/after 表示
    samples = [1, 3, 4, 6, 7, 10, 12, 13, 17, 46]
    print('=== 代表10件 before/after ===')
    for ba in before_after:
        if ba['rank'] in samples:
            print()
            print(f"#{ba['rank']:2d} {ba['app_name']}")
            print(f"  hook:")
            print(f"    BEFORE: {ba['before_hook']}")
            print(f"    AFTER:  {ba['after_hook']}")
            print(f"  desc:")
            print(f"    BEFORE: {ba['before_desc']}")
            print(f"    AFTER:  {ba['after_desc']}")


if __name__ == '__main__':
    main()
